package repository

import (
	"fmt"
	"github.com/jmoiron/sqlx"
	rest_api_auth "github.com/mazhaboy/rest-api-auth"
)

type AuthPostgres struct {
	db *sqlx.DB
}

func NewAuthPostgres(db *sqlx.DB) *AuthPostgres {
	return &AuthPostgres{db: db}
}

